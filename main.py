class Item:
    def __init__(self, x=None, y=None, next=None):
        self.x = x
        self.y = y
        self.next = next


class List:
    def __init__(self):
        self.prev_nodes = None

    def path_finder(self, start, goal):
        current_nodes = [Item(start[0], start[1])]
        new_nodes = []
        self.prev_nodes = Item(start[0], start[1])
        while current_nodes:
            for elem in current_nodes:
                if elem.x > 0:
                    new_node = self.create_new_node(elem.x - 1, elem.y, new_nodes, goal, elem)
                    if new_node:
                        return new_node
                if elem.x < WIDTH - 1:
                    new_node = self.create_new_node(elem.x + 1, elem.y, new_nodes, goal, elem)
                    if new_node:
                        return new_node
                if elem.y > 0:
                    new_node = self.create_new_node(elem.x, elem.y - 1, new_nodes, goal, elem)
                    if new_node:
                        return new_node
                if elem.y < HEIGHT - 1:
                    new_node = self.create_new_node(elem.x, elem.y + 1, new_nodes, goal, elem)
                    if new_node:
                        return new_node
            current_nodes.clear()
            current_nodes.extend(new_nodes)
            new_nodes.clear()
        return None

    def create_new_node(self, x, y, new_nodes, goal, elem):
        current_node = self.prev_nodes
        while current_node:
            if (current_node.x, current_node.y) != (x, y):
                current_node = current_node.next
            else:
                return None
        if LABIRINT[y][x] != 1:
            new_node = Item(x, y, elem)
            if goal == (x, y):
                return new_node
            new_nodes.append(new_node)
            prev_node = Item(x, y, self.prev_nodes)
            self.prev_nodes = prev_node
        return None


